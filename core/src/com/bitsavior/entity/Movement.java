package com.bitsavior.entity;

/**
 * represents the current movement direction of a moving entity
 */
public enum Movement
{
    /**
     * left direction
     */
    LEFT,
    /**
     * right direction
     */
    RIGHT,
    /**
     * up direction
     */
    UP,
    /**
     * down direction
     */
    DOWN,
    /**
     * no movement
     */
    UNMOVED
}
